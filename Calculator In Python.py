number_1 = int(input('Please, Enter the first number: '))
number_2 = int(input('Please, Enter the second number: '))
 

print('{} + {} = '.format(number_1, number_2))
print(number_1 + number_2)
 

print('{} - {} = '.format(number_1, number_2))
print(number_1 - number_2)
 
print('{} * {} = '.format(number_1, number_2))
print(number_1 * number_2)
 
print('{} / {} = '.format(number_1, number_2))
print(number_1 / number_2)
